import type { ApplicationRecord, SessionSummary, ProfessionalProfile } from '@job-agent/core';

/** Status badge for Markdown */
const STATUS_EMOJI: Record<string, string> = {
  applied: '✅',
  easy_apply_not_available: '⚠️',
  already_applied: '🔁',
  failed: '❌',
  skipped_low_score: '⏭️',
};

/** Status badge for HTML */
const STATUS_HTML_CLASS: Record<string, string> = {
  applied: 'badge-success',
  easy_apply_not_available: 'badge-warning',
  already_applied: 'badge-info',
  failed: 'badge-danger',
  skipped_low_score: 'badge-secondary',
};

/**
 * Generates the Markdown report for a completed session.
 * @param records - All application records from the session.
 * @param summary - Session summary statistics.
 * @param profile - The candidate's professional profile.
 * @returns A Markdown string.
 */
export function generateMarkdownReport(
  records: ApplicationRecord[],
  summary: SessionSummary,
  profile: ProfessionalProfile
): string {
  const duration = Math.round(summary.durationSeconds / 60);

  const lines: string[] = [
    `# LinkedIn Job Application Report`,
    ``,
    `> Generated: ${new Date(summary.sessionEndedAt).toLocaleString()}`,
    ``,
    `## 👤 Candidate`,
    ``,
    `| Field | Value |`,
    `|-------|-------|`,
    `| Name | ${profile.fullName} |`,
    `| Seniority | ${profile.seniority} |`,
    `| Experience | ${profile.yearsOfExperience} years |`,
    `| Tech Stack | ${profile.techStack.slice(0, 8).join(', ')} |`,
    ``,
    `## 📊 Session Summary`,
    ``,
    `| Metric | Value |`,
    `|--------|-------|`,
    `| Jobs Found | ${summary.totalFound} |`,
    `| Jobs Scored | ${summary.totalScored} |`,
    `| Applications Sent | **${summary.totalApplied}** |`,
    `| Skipped (low score) | ${summary.totalSkipped} |`,
    `| Failed | ${summary.totalFailed} |`,
    `| Duration | ${duration} min |`,
    ``,
    `## 📋 Applications`,
    ``,
  ];

  // Applied first, then by score
  const sorted = [...records].sort((a, b) => {
    if (a.status === 'applied' && b.status !== 'applied') return -1;
    if (b.status === 'applied' && a.status !== 'applied') return 1;
    return b.job.compatibilityScore - a.job.compatibilityScore;
  });

  for (const record of sorted) {
    const { job, status, appliedAt, errorMessage } = record;
    const emoji = STATUS_EMOJI[status] ?? '❓';
    const score = job.compatibilityScore;

    lines.push(`### ${emoji} ${job.title}`);
    lines.push(`**${job.company}** · ${job.location} · ${job.modality}`);
    lines.push(`Score: **${score}/100** · Status: \`${status}\` · Applied: ${new Date(appliedAt).toLocaleString()}`);
    if (errorMessage) lines.push(`> ⚠️ Error: ${errorMessage}`);
    lines.push(`[View Job](${job.applyUrl})`);
    lines.push('');
  }

  lines.push(`---`);
  lines.push(`*Generated by Job Agent — Use responsibly. Respect LinkedIn's Terms of Service.*`);

  return lines.join('\n');
}

