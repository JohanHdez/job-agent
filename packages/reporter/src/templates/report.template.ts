    const { job, status, appliedAt } = record;
    const badgeClass = STATUS_HTML_CLASS[status] ?? 'badge-secondary';
    const scoreColor = job.compatibilityScore >= 80 ? '#10b981' : job.compatibilityScore >= 60 ? '#f59e0b' : '#ef4444';

